  function searchKnowledgeBase(query) {
    // 1. Intercept for MSDS searches first (unless it's a general conceptual query)
    if (!isConceptualQuery(query)) {
      const msdsResponse = searchMsdsDatabase(query);
      if (msdsResponse) {
        return msdsResponse;
      }
    }

    // 2. Standard rule matching
    const normalizedQuery = query.toLowerCase().trim();
    const cleanQuery = normalizedQuery
      .replace(/[؟?.,\/#!$%\^&\*;:{}=\-_`~()]/g, "")
      .replace(/\s+/g, " ");

    let bestMatch = null;
    let highestScore = 0;

    qaDatabase.forEach(item => {
      let score = 0;
      
      item.keywords.forEach(keyword => {
        if (cleanQuery.includes(keyword)) {
          score += 2;
        }
        
        const words = cleanQuery.split(' ').filter(w => w.length > 2);
        words.forEach(word => {
          if (keyword.includes(word)) {
            score += 0.5;
          }

          if (keyword.length > 2) {
            const sim = getStringSimilarity(word, keyword);
            if (sim >= 0.78) {
              score += sim * 1.5;
            } else {
              // For multi-word keywords, check similarity against individual words of the keyword
              const keywordWords = keyword.split(' ').filter(w => w.length > 2);
              if (keywordWords.length > 1) {
                let bestWordSim = 0;
                keywordWords.forEach(kwWord => {
                  const wSim = getStringSimilarity(word, kwWord);
                  if (wSim > bestWordSim) {
                    bestWordSim = wSim;
                  }
                });
                if (bestWordSim >= 0.78) {
                  score += bestWordSim * 0.75; // Moderate boost for matching a single word in a multi-word keyword with a typo
                }
              }
            }
          }
        });
      });

      if (score > highestScore) {
        highestScore = score;
        bestMatch = item;
      }
    });

    if (highestScore >= 1.5 && bestMatch) {
      return bestMatch.answer;
    }
    
    return fallbackText;
  }

